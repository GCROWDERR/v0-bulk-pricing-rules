// Types for Pricing Rules

export interface PricingRule {
  RuleId: number
  RuleDescription: string
  Lenders: string[]
  Fee: number
  Price: number
  CompPercent: number
  Active: boolean
  Disallow: boolean
  RuleIsDeleted: boolean
  // Step 1 fields
  LockPeriod: number
  FeeSet: string
  MICompany: string
  Rate: string
  MarginType: 'percentage' | 'flat'
  CompMin: number
  CompMax: number
  CompFlatFee: number
  FinalPriceMin: number
  FinalPriceMax: number
  HasSecondMortgage: boolean
  IgnoreNonEighthRates: boolean
  IncludeUFMIP: boolean
  MaxCashBack: number
  FinanceUFMIP: boolean
  // Step 2 fields
  LTVMin: number
  LTVMax: number
  FICOMin: number
  FICOMax: number
  LoanAmountMin: number
  LoanAmountMax: number
  PropertyTypes: string[]
  PropertyUsage: string[]
  LoanTypes: string[]
  QuotingChannels: string[]
  LockPeriods: number[]
  BorrowerFilters: string[]
  PointGroups: string[]
  States: string[]
  // Step 3 fields
  ProductFamilies: string[]
  ProductClasses: string[]
  ProductTypes: string[]
  ProductTerms: string[]
  // Step 4 fields
  StartDate: string | null
  EndDate: string | null
  StartTime: string | null
  EndTime: string | null
  // Visibility
  HideInQuoteAdjustments: boolean
}

export type DraftType = 'create' | 'update' | 'delete' | 'restore' | 'toggleActive'

export interface DraftEntry {
  ruleId: number
  type: DraftType
  originalRule?: PricingRule
  updatedRule: PricingRule
  changedFields?: string[]
}

