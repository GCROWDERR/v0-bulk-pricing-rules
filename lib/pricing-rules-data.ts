// Utility functions for formatting
export const formatCurrency = (value: number): string => {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  }).format(value)
}

export const formatPrice = (value: number): string => {
  return value.toFixed(3)
}

export const formatPercent = (value: number): string => {
  return `${value.toFixed(3)}%`
}

export const formatLoanAmount = (value: number): string => {
  if (value >= 1000000) {
    return `$${(value / 1000000).toFixed(2)}M`
  }
  return `$${(value / 1000).toFixed(0)}K`
}

// Get changed fields between two rules
export const getChangedFields = (original: PricingRule, updated: PricingRule): string[] => {
  const changes: string[] = []
  const keys = Object.keys(original) as (keyof PricingRule)[]
  
  for (const key of keys) {
    const origVal = original[key]
    const updVal = updated[key]
    
    if (Array.isArray(origVal) && Array.isArray(updVal)) {
      if (JSON.stringify(origVal.sort()) !== JSON.stringify(updVal.sort())) {
        changes.push(key)
      }
    } else if (origVal !== updVal) {
      changes.push(key)
    }
  }
  
  return changes
}
